    use ocl::{Buffer, Context, Device, Kernel, Platform, Program, Queue};

    pub struct JacobianToAffine {
        jac_x_buffer: Buffer<u8>,
        jac_y_buffer: Buffer<u8>,
        jac_z_buffer: Buffer<u8>,
        aff_x_buffer: Buffer<u8>,
        aff_y_buffer: Buffer<u8>,
        jacobian_to_affine_kernel: Kernel,
    }

    impl JacobianToAffine {
        pub fn new() -> Result<Self, String> {
            // CREATE OPENCL CONTEXT
            let (device, context, queue) = Self::get_device_context_and_queue()?;

            // Create buffers
            let jac_x_buffer = Self::new_buffer(&queue, 32)?; // Uint256 = 32 bytes
            let jac_y_buffer = Self::new_buffer(&queue, 32)?; // Uint256 = 32 bytes
            let jac_z_buffer = Self::new_buffer(&queue, 32)?; // Uint256 = 32 bytes
            let aff_x_buffer = Self::new_buffer(&queue, 32)?; // Uint256 = 32 bytes
            let aff_y_buffer = Self::new_buffer(&queue, 32)?; // Uint256 = 32 bytes

            let program = Self::build_program(device, context)?;

            // Create kernel
            let jacobian_to_affine_kernel = match Kernel::builder()
                .program(&program)
                .name("jacobian_to_affine_kernel")
                .queue(queue.clone())
                .arg(&jac_x_buffer)
                .arg(&jac_y_buffer)
                .arg(&jac_z_buffer)
                .arg(&aff_x_buffer)
                .arg(&aff_y_buffer)
                .global_work_size(1)
                .build()
            {
                Ok(kernel) => kernel,
                Err(e) => return Err("Error creating kernel: ".to_string() + &e.to_string()),
            };

            Ok(Self {
                jac_x_buffer,
                jac_y_buffer,
                jac_z_buffer,
                aff_x_buffer,
                aff_y_buffer,
                jacobian_to_affine_kernel,
            })
        }

        fn new_buffer(queue: &Queue, len: usize) -> Result<Buffer<u8>, String> {
            let buffer = match Buffer::<u8>::builder()
                .queue(queue.clone())
                .len(len)
                .build()
            {
                Ok(buffer) => buffer,
                Err(e) => return Err("Error creating buffer: ".to_string() + &e.to_string()),
            };
            Ok(buffer)
        }

        fn build_program(device: Device, context: Context) -> Result<Program, String> {
            let src = include_str!(concat!(env!("OUT_DIR"), "/jacobian_to_affine_kernel"));

            let program = match Program::builder().src(src).devices(device).build(&context) {
                Ok(program) => program,
                Err(e) => {
                    return Err("Error building OpenCL program: ".to_string() + &e.to_string())
                }
            };

            Ok(program)
        }

        fn get_device_context_and_queue() -> Result<(Device, Context, Queue), String> {
            let platform = match Platform::first() {
                Ok(platform) => platform,
                Err(e) => {
                    return Err("Error getting OpenCL platform: ".to_string() + &e.to_string())
                }
            };

            let device = match Device::first(platform) {
                Ok(device) => device,
                Err(e) => return Err("Error getting OpenCL device: ".to_string() + &e.to_string()),
            };

            let context = match Context::builder()
                .platform(platform)
                .devices(device)
                .build()
            {
                Ok(context) => context,
                Err(e) => {
                    return Err("Error building OpenCL context: ".to_string() + &e.to_string())
                }
            };

            let queue = Queue::new(&context, device, None).map_err(|e| e.to_string())?;

            Ok((device, context, queue))
        }

        fn write_to_buffer(
            &mut self,
            buffer: &Buffer<u8>,
            data: Vec<u8>,
        ) -> Result<(), String> {
            match buffer.write(&data[..]).enq() {
                Ok(_) => (),
                Err(e) => return Err("Error writing to buffer: ".to_string() + &e.to_string()),
            };
            Ok(())
        }

        fn read_from_buffer(&mut self, buffer: &Buffer<u8>) -> Result<Vec<u8>, String> {
            let mut data = vec![0u8; 32]; // Uint256 = 32 bytes
            match buffer.read(&mut data[..]).enq() {
                Ok(_) => (),
                Err(e) => return Err("Error reading from buffer: ".to_string() + &e.to_string()),
            };
            Ok(data)
        }

        fn convert(&mut self, jac_x: Vec<u8>, jac_y: Vec<u8>, jac_z: Vec<u8>) -> Result<(Vec<u8>, Vec<u8>), String> {
            if jac_x.len() != 32 || jac_y.len() != 32 || jac_z.len() != 32 {
                return Err(format!(
                    "All inputs must be 32 bytes long, got: x={}, y={}, z={}",
                    jac_x.len(), jac_y.len(), jac_z.len()
                ));
            }

            // Clone the buffers to avoid borrowing issues
            self.write_to_buffer(&self.jac_x_buffer.clone(), jac_x)?;
            self.write_to_buffer(&self.jac_y_buffer.clone(), jac_y)?;
            self.write_to_buffer(&self.jac_z_buffer.clone(), jac_z)?;

            // Execute kernel
            unsafe {
                match self.jacobian_to_affine_kernel.enq() {
                    Ok(_) => (),
                    Err(e) => return Err("Error executing kernel: ".to_string() + &e.to_string()),
                };
            }

            let aff_x = self.read_from_buffer(&self.aff_x_buffer.clone())?;
            let aff_y = self.read_from_buffer(&self.aff_y_buffer.clone())?;

            Ok((aff_x, aff_y))
        }
    }

